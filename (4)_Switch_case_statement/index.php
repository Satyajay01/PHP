<!-- Switch case statement condition is used to identify whether the condition matches the case or not -->

<?php
$color = "green";

switch ($color) {
  case "red":
    echo "Your favorite color is red!";
    break;
  case "blue":
    "Your favorite color is blue!";
    break;
  case "green":
    echo "Your favorite color is green!";
    break;
  default:
    echo "Your favorite color is neither red, blue, nor green!";
}


?>